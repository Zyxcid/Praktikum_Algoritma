#include <stdio.h> // library standar input output

// main function
main(void){
	// deklarasi variabel
	int a = 4;
	int b = 3;
	
	// operator aritmatika
	printf("a = %d\n", a);
	printf("b = %d\n", b);
	
	// operator pembanding
	printf("a > b = %d\n", a > b); // lebih besar
	printf("a < b = %d\n", a < b); // lebih kecil
	printf("a >= b = %d\n", a >= b); // lebih besar sama dengan
	printf("a <= b = %d\n", a <= b); // lebih kecil sama dengan
	printf("a == b = %d\n", a == b); // sama dengan
	printf("a != b = %d\n", a != b); // tidak sama dengan
	
}

